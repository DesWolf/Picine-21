int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int numb;

	i = 0;
	numb = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
		{
			numb++;
			i++;
		}
		else
			i++;
	}
	if (numb > 0)
		return (1);
	return (0);
}
