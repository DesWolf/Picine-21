int		ft_iterative_power(int nb, int power)
{
	int a;
	int b;

	a = 1;
	b = 1;
	if (power == 0)
	{
		return (0);
	}
	if (power != 0)
	{
		while (b <= power)
		{
			a = a * nb;
			b++;
		}
	}
	return (a);
}
